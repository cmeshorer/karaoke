export interface ExternalUrls {
  spotify: string; // The Spotify URL for the object.
}

export interface ExternalIds {
  isrc: string; // International Standard Recording Code.
  ean: string; // International Article Number.
  upc: string; // Universal Product Code.
}

export interface Restrictions {
  reason: "market" | "product" | "explicit"; // The reason for the restriction. Albums may be restricted if the content is not available in a given market, to the user's subscription type, or when the user's account is set to not play explicit content.
  // Additional reasons may be added in the future. Note: If you use this field, make sure that your application safely handles unknown values.
}

export interface Image {
  height: number | null; // The image height in pixels.
  url: string; // The source URL of the image.
  width: number | null; // The image width in pixels.
}

export interface Artist {
  external_urls: ExternalUrls; // Known external URLs for this artist.
  href: string; // A link to the Web API endpoint providing full details of the artist.
  id: string; // The Spotify ID for the artist.
  name: string; // The name of the artist.
  type: "artist"; // The object type.
  uri: string; // The Spotify URI for the artist.
}

export interface Album {
  album_type: "album" | "single" | "compilation"; // The type of the album.
  artists: Artist[]; // The artists of the album. Each artist object includes a link in href to more detailed information about the artist.
  available_markets: CountryCodes[]; // The markets in which the album is available: ISO 3166-1 alpha-2 country codes. NOTE: an album is considered available in a market when at least 1 of its tracks is available in that market.
  external_urls: ExternalUrls; // Known external URLs for this album.
  href: string; // A link to the Web API endpoint providing full details of the album.
  id: string; // The Spotify ID for the album.
  images: Image[]; // The cover art for the album in various sizes, widest first.
  name: string; // The name of the album. In case of an album takedown, the value may be an empty string.
  release_date: string; // The date the album was first released.
  release_date_precision: "year" | "month" | "day"; // The precision with which release_date value is known.
  restrictions: Restrictions; // Included in the response when a content restriction is applied.
  total_tracks: number; // The number of tracks in the album.
  type: string; // The object type.
  uri: string; // The Spotify URI for the album.
}

export enum Status {
  ADD = "add",
  ADDED = "added",
  REMOVE = "remove",
}

export interface Track {
  album: string;
  artists: string;
  artwork: string;
  duration: string;
  explicit: boolean;
  id: string;
  name: string;
  popularity: number;
  status: Status;
  year: string;
}

export type Tracks = Track[];
